#Tecnica de fatiamento para extraçõ de texto em yma String

nomeFilme = "Top Gun"

# Tudo a Partir da Primeira posição
print(nomeFilme[0:])
# Buscar toda a String a partir da ultima posição
print(nomeFilme[:7])
#Da terceira posição a a ultima
print(nomeFilme[2:])
#Buscar de dois em dois
print(nomeFilme[::2])
#Retornando somente ímpares
print(nomeFilme[1::2])
#inverter sequencia String
print(nomeFilme[::-1])